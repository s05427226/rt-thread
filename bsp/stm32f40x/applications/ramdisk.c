/* fat table */
static const rt_uint8_t _fat[4]={0xF8, 0xFF, 0xFF, 0xFF};

static rt_err_t rt_ramdisk_init(rt_device_t dev)
{

    return RT_EOK;
}

static rt_size_t rt_ramdisk_read(rt_device_t dev, rt_off_t pos, void* buffer, 
    rt_size_t size)
{
    RT_ASSERT(pos < BLOCK_COUNT);

    rt_memcpy(buffer, &ramdisk[pos * SECTOR_SIZE], size * SECTOR_SIZE);
    
    return SECTOR_SIZE;
}

static rt_size_t rt_ramdisk_write (rt_device_t dev, rt_off_t pos, 
    const void* buffer, rt_size_t size)
{
    RT_ASSERT(pos < BLOCK_COUNT);

    rt_memcpy(&ramdisk[pos * SECTOR_SIZE], buffer, size * SECTOR_SIZE);    

    return SECTOR_SIZE;
}

static rt_err_t rt_ramdisk_control(rt_device_t dev, int cmd, void *args)
{    
    /* check parameter */
    RT_ASSERT(dev != RT_NULL);

    if (cmd == RT_DEVICE_CTRL_BLK_GETGEOME)
    {
        struct rt_device_blk_geometry *geometry;

        geometry = (struct rt_device_blk_geometry *)args;
        if (geometry == RT_NULL) return -RT_ERROR;

        geometry->bytes_per_sector = SECTOR_SIZE;
        geometry->block_size = SECTOR_SIZE;
        geometry->sector_count = BLOCK_COUNT;
    }

    return RT_EOK;
}

rt_err_t rt_hw_ramdisk_init(void)
{
    /* format disk */
    rt_memset(ramdisk, '\0', sizeof(ramdisk));
    rt_memcpy(&ramdisk[512 * 0], _dbr, 512);
    rt_memcpy(&ramdisk[512 * 1], _fat, 4);
    rt_memcpy(&ramdisk[512 * 2], _fat, 4); 

    device.type = RT_Device_Class_Block;
    device.rx_indicate = RT_NULL;
    device.tx_complete = RT_NULL;

    device.init = rt_ramdisk_init;
    device.open = RT_NULL;
    device.close = RT_NULL;
    device.read = rt_ramdisk_read;
    device.write = rt_ramdisk_write;
    device.control = rt_ramdisk_control;   

    /* register a block device */
    return rt_device_register(&device, "ramdisk", RT_DEVICE_FLAG_RDWR);
}

